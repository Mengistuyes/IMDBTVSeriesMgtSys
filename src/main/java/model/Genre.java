package model;

public class Genre {
	private int GenreId;
	private String GenreName;
	Genre()
	{
		
	}
	public int getGenreId() {
		return GenreId;
	}
	public void setGenreId(int genreId) {
		GenreId = genreId;
	}
	public String getGenreName() {
		return GenreName;
	}
	public void setGenreName(String genreName) {
		GenreName = genreName;
	}
	
}
